//here we will show how to pop, push and shift


//here pop will remove one and one elements from end and if we use 'variable.shift()' then it will remove elements from start


let numbers = [2, 3, 4, 5, 6, 90, 100]
// console.log(numbers);
numbers.pop();//remove element from end
numbers.pop();
numbers.shift();//remove element from start
const out = numbers.pop();//we can also put them in a variable

console.log(numbers);
console.log(out);

